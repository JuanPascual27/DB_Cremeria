    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String n;
        Object [] data = new Object[6];
        n = JOptionPane.showInputDialog("Nombre a Buscar: ");
        data = e.Buscar(n);
        if(data[0].toString().isEmpty())//Objects.equals(null,data)
            {
              jTextField1.setText("");
              jTextField2.setText("");
              jTextField4.setText("");
              jTextField3.setText("");
              jTextField5.setText("");
              jTextField6.setText("");
             JOptionPane.showMessageDialog(null, "Usuario NO encontrado");
            }
           else
           {
              jTextField1.setText(data[0].toString());
              jTextField2.setText(data[1].toString());
              jTextField3.setText(data[2].toString());
              jTextField4.setText(data[3].toString());
              jTextField5.setText(data[4].toString());
              jTextField6.setText(data[5].toString());
              
           }
    }//GEN-LAST:event_jButton2ActionPerformed

